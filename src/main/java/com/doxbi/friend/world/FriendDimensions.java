    public static boolean sendHomeFromDream(ServerPlayer player, BlockPos preferredTarget) {
        ServerLevel overworld = player.server.getLevel(Level.OVERWORLD);
        if (overworld == null) {
            return false;
        }
        overworld.setDayTime(1000L);
        BlockPos target = preferredTarget;
        if (target == null) {
            target = player.getRespawnPosition();
        }
        if (target == null) {
            target = overworld.getSharedSpawnPos();
        }
        CompoundTag data = player.getPersistentData();
        float yaw = data.contains("friend_dream_return_yaw") ? data.getFloat("friend_dream_return_yaw") : player.getYRot();
        float pitch = data.contains("friend_dream_return_pitch") ? data.getFloat("friend_dream_return_pitch") : 0.0F;
        clearDreamSession(player);
        player.teleportTo(overworld, target.getX() + 0.5D, target.getY() + 1.0D, target.getZ() + 0.5D, yaw, pitch);
        player.addEffect(new MobEffectInstance(MobEffects.WEAKNESS, 20 * 12, 0, false, false));
        return true;
    }

