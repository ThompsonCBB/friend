    private static void clearLegacyDreamCorridor(ServerLevel level) {
        if (!level.getBlockState(DREAM_MARKER).is(Blocks.BEDROCK) && !level.getBlockState(DREAM_VERSION_MARKER).is(Blocks.BEDROCK)) {
            return;
        }
        for (int x = -6; x <= 6; x++) {
            for (int z = -12; z <= DREAM_LENGTH + 6; z++) {
                for (int y = 78; y <= 87; y++) {
                    level.setBlock(new BlockPos(x, y, z), Blocks.AIR.defaultBlockState(), 2);
                }
            }
        }
        level.setBlock(DREAM_MARKER, Blocks.AIR.defaultBlockState(), 3);
        level.setBlock(DREAM_VERSION_MARKER, Blocks.AIR.defaultBlockState(), 3);
    }

    private static int dreamHash(int x, int z) {
        int h = x * 73428767 ^ z * 912931 ^ 0x5f3759df;
        h ^= h >>> 13;
        h *= 1274126177;
        h ^= h >>> 16;
        return h & 0x7fffffff;
    }

