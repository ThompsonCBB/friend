    private static void buildParadiseChoice(ServerLevel level) {
        int y = PARADISE_START.getY() + 1;
        // Small clean choice area, not a portal room.
        for (int x = -9; x <= 9; x++) {
            for (int z = 4; z <= 14; z++) {
                level.setBlock(new BlockPos(x, y - 1, z), Blocks.SMOOTH_QUARTZ.defaultBlockState(), 3);
                level.setBlock(new BlockPos(x, y, z), Blocks.AIR.defaultBlockState(), 3);
                level.setBlock(new BlockPos(x, y + 1, z), Blocks.AIR.defaultBlockState(), 3);
            }
        }
        BlockState button = Blocks.STONE_BUTTON.defaultBlockState()
                .setValue(ButtonBlock.FACE, AttachFace.FLOOR)
                .setValue(ButtonBlock.FACING, Direction.NORTH);
        level.setBlock(new BlockPos(-5, y, 10), button, 3);
        level.setBlock(new BlockPos(5, y, 10), button, 3);
        level.setBlock(new BlockPos(-5, y - 1, 10), Blocks.QUARTZ_BLOCK.defaultBlockState(), 3);
        level.setBlock(new BlockPos(5, y - 1, 10), Blocks.QUARTZ_BLOCK.defaultBlockState(), 3);
        level.setBlock(new BlockPos(-5, y, 7), Blocks.LECTERN.defaultBlockState(), 3);
        level.setBlock(new BlockPos(5, y, 7), Blocks.LECTERN.defaultBlockState(), 3);
    }

    private static void buildSkyChoice(ServerLevel level, BlockPos center, boolean good) {
        BlockState frame = good ? Blocks.SEA_LANTERN.defaultBlockState() : Blocks.CRYING_OBSIDIAN.defaultBlockState();
        BlockState pane = good ? Blocks.LIGHT_BLUE_STAINED_GLASS.defaultBlockState() : Blocks.BLACK_STAINED_GLASS.defaultBlockState();
        for (int x = -3; x <= 3; x++) {
            for (int y = 0; y <= 5; y++) {
                boolean edge = Math.abs(x) == 3 || y == 0 || y == 5;
                level.setBlock(center.offset(x, y, 0), edge ? frame : pane, 3);
            }
        }
    }

    private static void clearLegacyBarrierMarkers(ServerLevel level) {
        if (level.getBlockState(LEGACY_MARKER).is(Blocks.BARRIER)) {
            level.setBlock(LEGACY_MARKER, Blocks.AIR.defaultBlockState(), 3);
        }
        if (level.getBlockState(LEGACY_VERSION_MARKER).is(Blocks.BARRIER)) {
            level.setBlock(LEGACY_VERSION_MARKER, Blocks.AIR.defaultBlockState(), 3);
        }
    }
