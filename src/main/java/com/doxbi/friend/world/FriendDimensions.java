    public static void tickDreamField(ServerPlayer player) {
        if (!player.level().dimension().equals(DREAM) || !(player.level() instanceof ServerLevel level)) {
            return;
        }
        level.setDayTime(6000L);
        level.setWeatherParameters(20 * 60 * 20, 0, false, false);
        sanitizeDreamSpawn(level);
        buildDreamExit(level, dreamExit(0));
        purgeDreamMobs(level, player.blockPosition());
    }

    public static boolean shouldAutoLeaveDream(ServerPlayer player) {
        if (!(player.level() instanceof ServerLevel level) || !level.dimension().equals(DREAM)) {
            return false;
        }
        CompoundTag data = player.getPersistentData();
        long exitAt = data.getLong("friend_dream_exit_at");
        if (exitAt > 0L && level.getGameTime() >= exitAt) {
            return true;
        }
        return isNearDreamExit(player.blockPosition(), data.getInt("friend_dream_variant"));
    }

    public static boolean isDreamExitDoor(BlockPos pos, int variant) {
        BlockPos exit = dreamExit(variant);
        return pos.equals(exit) || pos.equals(exit.above());
    }

    public static boolean isNearDreamExit(BlockPos pos, int variant) {
        return pos.closerThan(dreamExit(variant), 2.15D);
    }

