    public static boolean sendToDream(ServerPlayer player, int variant) {
        ServerLevel dream = dreamLevel(player.server);
        if (dream == null) {
            return false;
        }

        BlockPos returnPos = player.blockPosition();
        float returnYaw = player.getYRot();
        float returnPitch = player.getXRot();

        player.stopSleepInBed(true, true);

        // Dream terrain is generated by data/friend/dimension/dream.json.
        // Code only guarantees the single exit door in front of the player.
        int realm = 0;
        BlockPos start = dreamStart(realm);
        dream.setDayTime(6000L);
        dream.setWeatherParameters(20 * 60 * 20, 0, false, false);
        sanitizeDreamSpawn(dream);
        buildDreamExit(dream, dreamExit(realm));
        purgeDreamMobs(dream, DREAM_START);

        CompoundTag data = player.getPersistentData();
        data.putBoolean("friend_dream_session", true);
        data.putInt("friend_dream_variant", realm);
        data.putInt("friend_dream_return_x", returnPos.getX());
        data.putInt("friend_dream_return_y", returnPos.getY());
        data.putInt("friend_dream_return_z", returnPos.getZ());
        data.putFloat("friend_dream_return_yaw", returnYaw);
        data.putFloat("friend_dream_return_pitch", returnPitch);
        data.putLong("friend_dream_exit_at", dream.getGameTime() + 20L * (120 + player.getRandom().nextInt(61)));
        data.putInt("friend_dream_last_chunk_x", Integer.MIN_VALUE);
        data.putInt("friend_dream_last_chunk_z", Integer.MIN_VALUE);

        player.teleportTo(dream, start.getX() + 0.5D, DREAM_START.getY() + 1.0D, start.getZ() + 0.5D, 0.0F, 0.0F);
        player.addEffect(new MobEffectInstance(MobEffects.NIGHT_VISION, 20 * 180, 0, false, false));
        return true;
    }

