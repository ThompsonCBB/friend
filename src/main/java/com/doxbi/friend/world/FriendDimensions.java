    private static void sanitizeDreamSpawn(ServerLevel level) {
        // The Dream terrain comes from the flat generator. This method only removes old junk
        // from previously generated test worlds around the spawn/exit so the player never
        // spawns in barriers, flowing water, or leftover block portals.
        clearDreamJunk(level, DREAM_START, 5);
        clearDreamJunk(level, dreamExit(0), 4);
    }

    private static void clearDreamJunk(ServerLevel level, BlockPos center, int radius) {
        int floorY = DREAM_START.getY() - 1;
        for (int x = -radius; x <= radius; x++) {
            for (int z = -radius; z <= radius; z++) {
                for (int y = floorY + 1; y <= floorY + 4; y++) {
                    BlockPos pos = new BlockPos(center.getX() + x, y, center.getZ() + z);
                    BlockState state = level.getBlockState(pos);
                    if (state.is(Blocks.BARRIER) || state.is(Blocks.WATER) || state.is(Blocks.LAVA)
                            || state.is(Blocks.NETHER_PORTAL) || state.is(Blocks.END_PORTAL)
                            || state.is(Blocks.END_PORTAL_FRAME)) {
                        level.setBlock(pos, Blocks.AIR.defaultBlockState(), 3);
                    }
                }
            }
        }
    }

    private static void purgeDreamMobs(ServerLevel level, BlockPos center) {
        AABB area = new AABB(center).inflate(96.0D, 48.0D, 96.0D);
        for (Mob mob : level.getEntitiesOfClass(Mob.class, area, mob -> !(mob instanceof net.minecraft.world.entity.animal.Animal))) {
            mob.discard();
        }
    }

    private static BlockPos dreamExitMarker(BlockPos exit) {
        // Hidden one-shot marker. If the player breaks the exit door, this marker prevents
        // Dream tick/setup code from rebuilding it every tick and duplicating door items.
        return new BlockPos(exit.getX(), DREAM_START.getY() - 3, exit.getZ());
    }

    private static boolean isValidDreamExitDoor(ServerLevel level, BlockPos lowerPos) {
        BlockState lower = level.getBlockState(lowerPos);
        BlockState upper = level.getBlockState(lowerPos.above());
        return lower.is(Blocks.DARK_OAK_DOOR)
                && upper.is(Blocks.DARK_OAK_DOOR)
                && lower.hasProperty(DoorBlock.HALF)
                && upper.hasProperty(DoorBlock.HALF)
                && lower.getValue(DoorBlock.HALF) == DoubleBlockHalf.LOWER
                && upper.getValue(DoorBlock.HALF) == DoubleBlockHalf.UPPER;
    }

    private static void buildDreamExit(ServerLevel level, BlockPos exit) {
        int y = DREAM_START.getY();
        BlockPos lowerPos = new BlockPos(exit.getX(), y, exit.getZ());
        BlockPos upperPos = lowerPos.above();
        BlockPos marker = dreamExitMarker(exit);

        // IMPORTANT:
        // Build the Dream exit only once. Rebuilding a vanilla DoorBlock while it is being
        // broken causes the lower/upper half to update repeatedly, which can duplicate doors.
        if (level.getBlockState(marker).is(Blocks.BEDROCK)) {
            return;
        }

        if (isValidDreamExitDoor(level, lowerPos)) {
            level.setBlock(marker, Blocks.BEDROCK.defaultBlockState(), 2);
            return;
        }

        // Clean a tiny area once. Use flag 2 where possible so this setup pass does not behave
        // like repeated player block-breaking.
        level.setBlock(lowerPos, Blocks.AIR.defaultBlockState(), 2);
        level.setBlock(upperPos, Blocks.AIR.defaultBlockState(), 2);
        level.setBlock(lowerPos.below(), Blocks.FARMLAND.defaultBlockState(), 2);

        // One readable exit door only. No portal frame, no random doors, no decorative block structures.
        for (int dx = -1; dx <= 1; dx++) {
            for (int dz = -1; dz <= 1; dz++) {
                for (int dy = 0; dy <= 2; dy++) {
                    BlockPos clear = new BlockPos(exit.getX() + dx, y + dy, exit.getZ() + dz);
                    if (!clear.equals(lowerPos) && !clear.equals(upperPos)) {
                        BlockState existing = level.getBlockState(clear);
                        if (existing.is(Blocks.BARRIER) || existing.is(Blocks.WATER) || existing.is(Blocks.LAVA)
                                || existing.is(Blocks.NETHER_PORTAL) || existing.is(Blocks.END_PORTAL)
                                || existing.is(Blocks.END_PORTAL_FRAME)) {
                            level.setBlock(clear, Blocks.AIR.defaultBlockState(), 2);
                        }
                    }
                }
            }
        }

        BlockState lower = Blocks.DARK_OAK_DOOR.defaultBlockState()
                .setValue(DoorBlock.FACING, Direction.SOUTH)
                .setValue(DoorBlock.HALF, DoubleBlockHalf.LOWER);
        BlockState upper = Blocks.DARK_OAK_DOOR.defaultBlockState()
                .setValue(DoorBlock.FACING, Direction.SOUTH)
                .setValue(DoorBlock.HALF, DoubleBlockHalf.UPPER);

        level.setBlock(lowerPos, lower, 2);
        level.setBlock(upperPos, upper, 2);
        level.setBlock(marker, Blocks.BEDROCK.defaultBlockState(), 2);
    }

    public static void ensureVoidCorridor(ServerLevel level) {
        clearLegacyBarrierMarkers(level);
        if (level.getBlockState(VOID_MARKER).is(Blocks.BEDROCK) && level.getBlockState(VOID_VERSION_MARKER).is(Blocks.BEDROCK)) {
            return;
        }
        level.setBlock(VOID_MARKER, Blocks.BEDROCK.defaultBlockState(), 3);
        level.setBlock(VOID_VERSION_MARKER, Blocks.BEDROCK.defaultBlockState(), 3);
        for (int z = -16; z <= VOID_LENGTH + 4; z++) {
            for (int x = -5; x <= 5; x++) {
                for (int y = 78; y <= 88; y++) {
                    BlockPos pos = new BlockPos(x, y, z);
                    boolean shell = x == -5 || x == 5 || y == 78 || y == 88 || z == -16 || z == VOID_LENGTH + 4;
                    level.setBlock(pos, shell ? Blocks.BEDROCK.defaultBlockState() : Blocks.AIR.defaultBlockState(), 2);
                }
            }
        }
        for (int z = 20; z <= VOID_LENGTH; z += 80) {
            level.setBlock(new BlockPos(-4, 82, z), Blocks.SCULK.defaultBlockState(), 2);
            level.setBlock(new BlockPos(4, 82, z + 12), Blocks.SCULK.defaultBlockState(), 2);
        }
    }

    public static void ensureSky(ServerLevel level) {
        clearLegacyBarrierMarkers(level);
        if (level.getBlockState(SKY_MARKER).is(Blocks.BEDROCK) && level.getBlockState(SKY_VERSION_MARKER).is(Blocks.BEDROCK)) {
            return;
        }
        level.setBlock(SKY_MARKER, Blocks.BEDROCK.defaultBlockState(), 3);
        level.setBlock(SKY_VERSION_MARKER, Blocks.BEDROCK.defaultBlockState(), 3);
        int y = SKY_START.getY();
        for (int x = -SKY_RADIUS; x <= SKY_RADIUS; x++) {
            for (int z = -SKY_RADIUS; z <= SKY_RADIUS; z++) {
                boolean edge = Math.abs(x) == SKY_RADIUS || Math.abs(z) == SKY_RADIUS;
                level.setBlock(new BlockPos(x, y, z), Blocks.BARRIER.defaultBlockState(), 2);
                if (edge) {
                    for (int h = 1; h <= 7; h++) {
                        level.setBlock(new BlockPos(x, y + h, z), Blocks.BARRIER.defaultBlockState(), 2);
                    }
                }
                if ((x * x + z * z) % 173 == 0) {
                    level.setBlock(new BlockPos(x, y + 1, z), Blocks.WHITE_STAINED_GLASS.defaultBlockState(), 2);
                }
            }
        }
        buildSkyChoice(level, new BlockPos(-36, y + 1, 56), true);
        buildSkyChoice(level, new BlockPos(36, y + 1, 56), false);
        for (int z = 5; z <= 52; z += 7) {
            level.setBlock(new BlockPos(-36, y + 1, z), Blocks.SEA_LANTERN.defaultBlockState(), 2);
            level.setBlock(new BlockPos(36, y + 1, z), Blocks.CRYING_OBSIDIAN.defaultBlockState(), 2);
        }
    }

